#to iterate over items of a collection,such as string which is a sequence of a character
#first iteration is P, second is h
for item in 'Phyton': 
    print(item)

for item in ['Mosh', 'John', 'Sarah']:
    print(item)

for item in range(10):
    print(item) 

for item in range(5, 10):
    print(item) 

for item in range(5, 10, 2):
    print(item) 